package day2;

public class CharEX {
    public static void main(String[] args) {
        char c1 = 'A';
        System.out.println(c1);

        char c2 = 65;
        System.out.println(c2);

        char c3 = ' ';
        System.out.println(c3);

        //char 문자데이터 초기화 공백의 유니코드값 32으로 힌다. '' 안되고 빈(empty)
        char c4 = '가';
        System.out.println(c4);


        char c5 = 44032;
        System.out.println(c5);
        //   '가'의 유니코드값




    }
}
